-- **********************************************************************
-- PROCEDURE INFAddUpdateRouteRestrictions
-- **********************************************************************
ALTER PROCEDURE dbo.INFAddUpdateRouteRestrictions
(
    @RouteCode          varchar(50),
    @ATB2Text           varchar(50),
    @PortalDescription  varchar(100)
) AS
BEGIN
    SET NOCOUNT ON
    IF EXISTS(SELECT 1
                FROM dbo.RouteRestrictions
               WHERE RouteCode = @RouteCode)
        BEGIN
          UPDATE dbo.RouteRestrictions
             SET ATB2Text = @ATB2Text,
                 PortalDescription = @PortalDescription
               WHERE RouteCode = @RouteCode
           PRINT 'Updated INFAddUpdateRouteRestrictions RouteCode ' + @RouteCode
        END
    ELSE
        BEGIN

            INSERT INTO dbo.RouteRestrictions 
            (
                [RouteCode], 
                [ATB2Text], 
                [PortalDescription]
            )
            VALUES
            (
                @RouteCode, 
                @ATB2Text, 
                @PortalDescription
            )
            PRINT 'Inserted INFAddUpdateRouteRestrictions RouteCode ' + @RouteCode
        END
    --END IF    
END
